#include<iostream>
using namespace std;
int main()
{
	int n;
	cin>>n;
	if(n==1) cout<<0<<" "<<0;
	else if(n%4==3)
	{
		int a=n/4+1;
		cout<<a<<" "<<a;
	}
	else if(n%4==2)
	{
		int a=n/4+1;
		cout<<a<<" "<<1-a;
	}
	else if(n%4==1)
	{
		int a=n/4;
		cout<<-a<<" "<<-a;
	}
	else if(n%4==0)
	{
		int a=n/4;
		cout<<-a<<" "<<a;
	}
	return 0;
}
